public class DoWhile {

	public static void main(String[] args) {
		// while loop first checks condition 
		//and only then executes bloke of code
		
		
		
		
		
		
		
		
			
		// print even number from 70 to 30;
			
		int a=70;
			
		do {
			System.out.print(a+" ");
			a+=-2; //68,66,64 etc
		}while (a>=30);		
			
	}

}
